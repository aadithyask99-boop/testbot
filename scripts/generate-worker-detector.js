// ============================================================
// generate-worker-detector.js (Session 7)
// ============================================================
// Manual-run helper, NOT part of CI/build. Reads lib/detector.js's
// AI_CRAWLERS array and emits a compact JS array literal suitable for
// pasting into worker/index.js's BOT_PATTERNS constant.
//
// Run whenever lib/detector.js's self-identifying crawler list changes
// meaningfully (new major AI crawler added/removed):
//
//   node scripts/generate-worker-detector.js > /tmp/bot-patterns.js
//
// Then paste the output into worker/index.js, replacing the existing
// BOT_PATTERNS constant. See WORKER_SDK_SPEC.md for why this is a
// manual step rather than automated.
//
// v1 scope: only entries with simple string `patterns` (substring
// match against the User-Agent header) are included. Behavioral-only
// detection (lib/behavioural.js, lib/combined-detector.js's anonymous
// crawler path) is OUT OF SCOPE for the embedded Worker check — see
// WORKER_SDK_SPEC.md "Deferred to v2".
// ============================================================

const { AI_CRAWLERS } = require('../lib/detector');

const simple = AI_CRAWLERS.filter(
  c => Array.isArray(c.patterns) && c.patterns.every(p => typeof p === 'string')
);

const entries = simple.map(c => ({
  name: c.name,
  patterns: c.patterns,
  type: c.type,
  cloakingRisk: c.cloakingRisk === true,
}));

const skipped = AI_CRAWLERS.length - simple.length;

console.log('// Generated by scripts/generate-worker-detector.js from lib/detector.js');
console.log('// ' + simple.length + ' of ' + AI_CRAWLERS.length + ' AI_CRAWLERS entries included');
console.log('// (' + skipped + ' skipped — non-string patterns, regenerate after checking lib/detector.js)');
console.log('// Regenerate: node scripts/generate-worker-detector.js');
console.log('const BOT_PATTERNS = ' + JSON.stringify(entries, null, 2) + ';');
